Заголовок : Заголовок секции задается параметром `TITLE` из массива `arParams`.
Список новостей : Создается неупорядоченный список (`<ul>`), где каждый элемент списка представляет собой новость. Для каждого элемента массива `arResult['ITEMS']`:
Заголовок новости (ссылка на страницу новости) отображается с помощью тега `<a>`.
Краткое содержание новости (`PREVIEW_TEXT`) выводится внутри параграфа (`<p>`).
Дата публикации (`DISPLAY_ACTIVE_FROM`) выводится в небольшом тексте (`<small>`).
Параметры компонента:
`TITLE` (строка): Заголовок списка новостей.
*/